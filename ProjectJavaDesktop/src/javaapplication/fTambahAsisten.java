    private void jBtnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnSimpanMouseClicked
        // TODO add your handling code here:
        String sql="INSERT into tbl_asisten (kode_asisten, nim, nama, alamat, email, telp)VALUE('"+
                    jTxtKdAsisten.getText()+"','"+
                    jTxtNim.getText()+"','"+
                    jTxtNama.getText()+"','"+
                    jTextAreaAlamat.getText()+"','"+
                    jTxtEmail.getText()+"','"+
                    jTxtTelp.getText()+"');";
                     
        try {
            kon.konek();
            kon.st = kon.conn.createStatement();
            kon.st.execute(sql); //menjalankan query
        } catch (SQLException e){
            setKosongkan();
            jTxtKdAsisten.requestFocus();
            return;
        }
        
        JOptionPane.showMessageDialog(this,"Data berhasil disimpan");
        setKosongkan();
        jTxtKdAsisten.requestFocus();
                    
    }//GEN-LAST:event_jBtnSimpanMouseClicked

