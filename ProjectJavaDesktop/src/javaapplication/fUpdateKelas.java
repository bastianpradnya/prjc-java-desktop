    private void jBtnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnUpdateMouseClicked
        // TODO add your handling code here:
        String sql="UPDATE tbl_kelas "+
                    "SET kode_kelas='"+jTxtKdKelas.getText()+"', "+
                    "kode_makul='"+jCbKdMakul.getSelectedItem().toString()+"', "+
                    "ruangan='"+jTxtRuangan.getText()+"', "+
                    "hari='"+jTxtHari.getText()+"', "+
                    "pukul='"+jTxtPukul.getText()+"' "+
                    " WHERE kode_kelas='"+jTxtKdKelas.getText()+"';";
                     
        try {
            kon.konek();
            kon.st = kon.conn.createStatement();
            kon.st.execute(sql); //menjalankan query
        } catch (SQLException e){
            System.err.println("SQLException");
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(this,"Data  gagal diupdate");
            setKosongkan();
            jTxtKdKelas.requestFocus();
            return;
        }
        
        JOptionPane.showMessageDialog(this,"Data berhasil diupdate");
        setKosongkan();
        jTxtKdKelas.requestFocus();
                    
    }//GEN-LAST:event_jBtnUpdateMouseClicked

    private void jBtnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCariActionPerformed
        // TODO add your handling code here:
        String sql="SELECT * FROM tbl_kelas WHERE kode_kelas='"+jTxtKdKelas.getText()+"'";
        try {
            kon.konek();
            kon.st = kon.conn.createStatement();
            kon.rs = kon.st.executeQuery(sql);
            
        } catch (SQLException ex){
            System.out.printf("Kesalahan Query" + sql);
        }
        
        int size = 0;
        try{
            while (kon.rs.next()){
                size++;
                jCbKdMakul.addItem(kon.rs.getString("kode_makul").toString());
                jTxtRuangan.setText(kon.rs.getString("ruangan").toString());
                jTxtHari.setText(kon.rs.getString("hari").toString());
                jTxtPukul.setText(kon.rs.getString("pukul").toString());
            }
            if(size==0){
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan");
                return;
            }
            kon.rs.close();
            kon.st.close();
        } catch(SQLException ex){
        
        }
    }//GEN-LAST:event_jBtnCariActionPerformed

